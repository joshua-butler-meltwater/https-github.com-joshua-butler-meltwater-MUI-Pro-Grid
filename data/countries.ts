export const getContinents = () => [...new Set(countries.map((row) => row.continent))];

// Filter data based on search and filters
export const filterCountryData = (
  allCountries: CountryData[], 
  search: string, 
  continents: string[]
): CountryData[] => {
  let filtered = allCountries;

  // Apply continent filter
  if (continents.length > 0) {
    filtered = filtered.filter((row) => continents.includes(row.continent));
  }

  // Apply search filter
  if (search) {
    const lowercasedFilter = search.toLowerCase();
    filtered = filtered.filter((item) => {
      return Object.keys(item).some((key) => {
        if (key === "id") return false;
        const value = item[key as keyof typeof item];
        return value && String(value).toLowerCase().includes(lowercasedFilter);
      });
    });
  }

  return filtered;
};